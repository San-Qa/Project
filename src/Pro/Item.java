package Pro;

public class Item {

	private int itno;
	private String itname;
	private String itdescrip;
	private double itprice;
	//Setter and Getter methods
	public int getItno() {
		return itno;
	}
	public void setItno(int itno) {
		this.itno = itno;
	}
	public String getItname() {
		return itname;
	}
	public void setItname(String itname) {
		this.itname = itname;
	}
	public String getItdescrip() {
		return itdescrip;
	}
	public void setItdescrip(String itdescrip) {
		this.itdescrip = itdescrip;
	}
	public double getItprice() {
		return itprice;
	}
	public void setItprice(double itprice) {
		this.itprice = itprice;
	}
	
	
	
}
